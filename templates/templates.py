    def print_char_skills(self, character_dict):
        if len(character_dict['skills']) > 0:
            skills_sheet = ["[SKILLS]  SKILLS:\n", ]
            skills = character_dict['skills']
            for l, j in skills.items():
                skills_sheet_a = []
                skills_sheet.append('\nᗘᗘ{} :\n'.format(l))
                for k in j.items():
                    skills_sheet_a.append('ᗘᗘᗘ{}\n'.format(k))
                skills_sheet.append("".join(sorted(skills_sheet_a)))

            return "".join(skills_sheet)
        else:
            skills_sheet = "   [SKILLS]  SKILLS: \n"
            return skills_sheet
        
    def print_char_merits(self, character_dict):
        if len(character_dict['merits']) > 0:
            merits_sheet = ["\n[MERITS]  MERITS:", ]
            merits = character_dict['merits']
            for l, j in merits.items():
                merits_sheet.append('\nᗘᗘ{}: {}\n'.format(l, j))
            return "".join(merits_sheet)
        else:
            merits_sheet = "   [MERITS]  MERITS: \n"
            return merits_sheet



